# 오늘은 파이썬을 설치하고, 개발환경도구인 파이참을 설치하고자 한다. 
# 파이썬을 설치하는 이유는 우리가 코딩한 소스를 컴파일해줄 인터프리터가 필요하기 때문이다. 
# vscode OR 파이참을 설치하는 이유는 좀 더 편한 환경에서 개발을 하기 위함이다. 
# 메모장을 쓸수도 있지만 우리가 한글이나 MS office를 쓰듯이...

 
# 1.파이썬 설치

#  1)구글에 python download 검색 첫 줄 클릭(www.python.org)

#  2)Download Python 클릭

#  3)다운로드 폴더에 저장된 설치파일을 실행

#  4)add python path 체크후에 Customize installation에 들어가 나머지 옵션 모두 클릭

#  5)install

 
# 2.vs code 설치
#   1)구글에 vscode 검색 첫 줄 클릭(code.visualstudio.com)

#   2)다운로드 버튼 클릭

#   3)체크박스 모두 선택 후 설치 진행

#   4)VScode 실행 후 Extention 클릭 후 python 검색(첫줄 나오는것 다운)

#   5) explorer 클릭



# 3.파이참 설치
#  1)구글에 pycharm download 검색 첫 줄 클릭

#  2)Community 버전 다운로드

#  3)다운로드 폴더에 저장된 설치파일을 실행

#  4)default 옵션으로 설치 진행

#  5)do not import 버튼 클릭 후 실행

#  6)NewProject 클릭 후 프로젝트 생성

#  7)default 설정으로 생성되어 열린 main.py 파일에서 오른쪽 상단에 재생버튼을 run 해보자. Hi, PyCharm 이라는 내용이 하단의 run탭에서 정상적으로 출력되고 있는 것을 확인할 수 있을 것이다.

